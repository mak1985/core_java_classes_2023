package com.company_name.day05;

public class ForLoop {
    public static void main(String[] args) {
        /**
         * syntax: for (initialization; condition; post incre/decre)
         * {
         * 	sout	- code
         * }
         */
        for (int i = 1; i <=100 ; i++)
        {
            System.out.println(i); //1; 2;
        }
    }
}
