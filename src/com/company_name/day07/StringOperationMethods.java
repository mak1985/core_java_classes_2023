package com.company_name.day07;

public class StringOperationMethods {
    public static void main(String[] args) {

        String s = "hello";
        char[ ] ch = { 'h', 'e', 'l', 'l', 'o'};
        String g = new String(ch);

        System.out.println(s); // hello
        System.out.println(ch);
        System.out.println(g);
    }
}
